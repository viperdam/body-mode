const LANGUAGES = ['ar', 'de', 'es', 'fr', 'hi', 'ja', 'ko', 'nl', 'pt', 'sw', 'tr', 'zh'];

function sortObjectKeys(obj) {
  return Object.keys(obj).sort().reduce((sorted, key) => {
    sorted[key] = obj[key];
    return sorted;
  }, {});
}

async function applyTranslations() {
  const enPath = path.join(TRANSLATIONS_DIR, 'en.json');
  const enData = JSON.parse(fs.readFileSync(enPath, 'utf8'));

  let totalUpdated = 0;

  for (const lang of LANGUAGES) {
    const langPath = path.join(TRANSLATIONS_DIR, `${lang}.json`);
    let langData = {};

    if (fs.existsSync(langPath)) {
      langData = JSON.parse(fs.readFileSync(langPath, 'utf8'));
    }

    let updated = 0;

    for (const [key, translations] of Object.entries(TRANSLATIONS)) {
      if (enData[key] && translations[lang]) {
        if (langData[key] === enData[key] || !langData[key]) {
          langData[key] = translations[lang];
          updated++;
        }
      }
    }

    const sorted = sortObjectKeys(langData);
    fs.writeFileSync(langPath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');
    console.log(`[${lang}] Updated ${updated} keys`);
    totalUpdated += updated;
  }

  console.log(`\n[settings-3] Total: ${totalUpdated} updates across ${LANGUAGES.length} languages`);
  console.log(`[settings-3] Keys in batch: ${Object.keys(TRANSLATIONS).length}`);
}

applyTranslations().catch(console.error);
